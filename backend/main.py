class ReasoningStep(BaseModel):
    thought: str
    action: Optional[str] = None
    observation: Optional[str] = None


class ForensicFinding(BaseModel):
    description: str
    location_normalized: list[int]  # [ymin, xmin, ymax, xmax] from 0-1000


