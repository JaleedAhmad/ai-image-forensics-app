MAX_FILE_SIZE = 5 * 1024 * 1024  # 5 MB
ALLOWED_MIME_TYPES = ["image/jpeg", "image/png", "image/webp"]


@app.post("/analyze_image/")
async def analyze_image(file: UploadFile = File(...)):
    if file.content_type not in ALLOWED_MIME_TYPES:
        raise HTTPException(
            status_code=415,
            detail="Unsupported file format. Please upload a JPEG, PNG, or WEBP.",
        )

    image_bytes = await file.read()
    if len(image_bytes) > MAX_FILE_SIZE:
        raise HTTPException(status_code=413, detail="File exceeds the 5MB size limit.")

    queue = asyncio.Queue()

    async def send_event(data: dict):
        await queue.put(f"data: {json.dumps(data)}\n\n")

    async def analysis_task():
        try:
            pil_img = Image.open(io.BytesIO(image_bytes)).convert("RGB")
            case_id = f"FF-{str(uuid.uuid4())[:8].upper()}"
            evidence_images = []

            metadata_finding = scan_metadata(pil_img)

            ela_bytes = get_ela_image(pil_img)
            ela_url = upload_to_gcs(ela_bytes, f"{case_id}-global-ela.jpg")
            evidence_images.append(
                ForensicEvidence(name="Global Error Level Analysis (ELA)", url=ela_url)
            )

            edge_bytes = get_edge_map(image_bytes)
            edge_url = upload_to_gcs(edge_bytes, f"{case_id}-edge-map.jpg")
            evidence_images.append(
                ForensicEvidence(name="Edge Detection Map", url=edge_url)
            )

            await send_event(
                {"stage": "cv_complete", "message": "ELA map and edge detection ready"}
            )

            await send_event(
                {
                    "stage": "agent_a_thinking",
                    "message": "Metadata analyst examining compression layers...",
                }
            )
            await send_event(
                {
                    "stage": "agent_b_thinking",
                    "message": "Geometric auditor scanning for plausibility failures...",
                }
            )

            from services.provider_router import run_vision_agents

            metadata = {
                "format": getattr(pil_img, "format", "Unknown"),
                "size": pil_img.size,
                "mode": pil_img.mode,
            }
            if metadata_finding:
                metadata["scan_result"] = metadata_finding

            report_a, report_b = await run_vision_agents(
                image_bytes, ela_bytes, edge_bytes, metadata
            )

            await send_event(
                {
                    "stage": "agents_complete",
                    "message": "Both specialists have filed their reports",
                }
            )

            await send_event(
                {
                    "stage": "arbitration_start",
                    "message": "Arbitrator weighing the evidence...",
                }
            )

            from services.agent_c import run_agent_c

            final_verdict = await run_agent_c(report_a, report_b)

            # Persist to Firestore
            if db is not None:
                try:
                    orig_url = upload_to_gcs(
                        image_bytes, f"{case_id}-original.jpg", file.content_type
                    )
                    doc_data = {
                        "case_id": case_id,
                        "verdict": final_verdict.verdict,
                        "confidence": final_verdict.confidence,
                        "final_verdict_data": final_verdict.model_dump(),
                        "evidence_images": [
                            img.model_dump() for img in evidence_images
                        ],
                        "original_image_url": orig_url,
                        "created_at": firestore.SERVER_TIMESTAMP,
                    }
                    db.collection("forensic_cases").document(case_id).set(doc_data)
                except Exception as e:
                    logger.error(f"Error saving to Firestore: {e}")

            payload_data = final_verdict.model_dump()
            payload_data["case_id"] = case_id
            payload_data["evidence_images"] = [
                img.model_dump() for img in evidence_images
            ]
            try:
                payload_data["original_image_url"] = orig_url
            except NameError:
                pass

            await send_event({"stage": "verdict_ready", "payload": payload_data})

        except Exception as e:
            logger.error(f"Error in analysis task: {e}")
            await send_event({"stage": "error", "message": str(e)})
        finally:
            await queue.put(None)

    async def generator():
        task = asyncio.create_task(analysis_task())
        while True:
            item = await queue.get()
            if item is None:
                break
            yield item
        await task

    return StreamingResponse(
        generator(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "Connection": "keep-alive",
            "X-Accel-Buffering": "no",
        },
    )


@app.get("/cases/{case_id}", response_model=ForensicAnalysisResult)
def get_case(case_id: str):
    if db is None:
        raise HTTPException(status_code=503, detail="Database not initialized.")

    doc_ref = db.collection("forensic_cases").document(case_id)
    doc = doc_ref.get()

    if not doc.exists:
        raise HTTPException(status_code=404, detail="Case not found")

    data = doc.to_dict()

    # Reconstruct perfectly
    return ForensicAnalysisResult(
        case_id=data.get("case_id", case_id),
        verdict=data.get("verdict", "Unknown"),
        confidence_level=data.get("confidence_level", "Low"),
        explanation=data.get("explanation", ""),
        detailed_findings=[
            ForensicFinding(**f) for f in data.get("detailed_findings", [])
        ],
        evidence_images=[
            ForensicEvidence(**img) for img in data.get("evidence_images", [])
        ],
        reasoning_steps=[],
    )
